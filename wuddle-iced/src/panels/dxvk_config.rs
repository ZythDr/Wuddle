// Config generator (pure function — no Iced dependency)
// ---------------------------------------------------------------------------

pub fn generate_conf(cfg: &DxvkConfig) -> String {
    let mut s = String::with_capacity(1024);

    s.push_str("# dxvk.conf — generated by Wuddle\n");
    s.push_str("# Turtle WoW D3D9 configuration\n");

    // --- Performance & Latency ---
    s.push_str("\n############################\n");
    s.push_str("# Performance & Latency\n");
    s.push_str("############################\n\n");

    let fps: u32 = cfg.max_frame_rate.trim().parse().unwrap_or(0);
    if fps > 0 {
        s.push_str("# Cap the frame rate. Values above ~245 can cause physics/timer issues in vanilla WoW.\n");
        s.push_str(&format!("d3d9.maxFrameRate = {}\n", fps));
    } else {
        s.push_str("# d3d9.maxFrameRate = 0  # Unlimited. A cap of 240 or less is recommended.\n");
    }

    s.push('\n');
    let latency: u32 = cfg.max_frame_latency.trim().parse().unwrap_or(1).clamp(1, 16);
    s.push_str("# Frames the GPU may buffer ahead. Fewer frames = lower input latency (1 is recommended).\n");
    s.push_str(&format!("d3d9.maxFrameLatency = {}\n", latency));

    match cfg.latency_sleep {
        TriState::Auto => {} // DXVK default — omit
        TriState::True => {
            s.push_str("\n# Enable NVAPI/Reflex latency sleep for reduced input latency on NVIDIA GPUs.\n");
            s.push_str("dxvk.latencySleep = True\n");
        }
        TriState::False => {
            s.push_str("\n# Disable NVAPI/Reflex latency sleep.\n");
            s.push_str("dxvk.latencySleep = False\n");
        }
    }

    // --- Async Shader Compilation ---
    s.push_str("\n############################\n");
    s.push_str("# Async Shader Compilation\n");
    s.push_str("############################\n\n");

    if cfg.enable_async {
        s.push_str("# Enable asynchronous shader compilation — the primary fix for D3D9 stutter in WoW.\n");
        s.push_str("# Requires the gplasync/async fork of DXVK. Has no effect on vanilla DXVK builds.\n");
        s.push_str("dxvk.enableAsync = true\n");
    } else {
        s.push_str("# dxvk.enableAsync = true  # Async disabled; enable for gplasync builds.\n");
    }

    // --- Shader Compilation ---
    s.push_str("\n############################\n");
    s.push_str("# Shader Compilation\n");
    s.push_str("############################\n\n");

    let threads: u32 = cfg.num_compiler_threads.trim().parse().unwrap_or(0);
    if threads > 0 {
        s.push_str("# Number of background threads used to compile graphics pipelines (0 = all available cores).\n");
        s.push_str(&format!("dxvk.numCompilerThreads = {}\n", threads));
        s.push('\n');
    }

    match cfg.enable_gpl {
        TriState::Auto => {} // DXVK default — omit
        TriState::True => {
            s.push_str("# Use the Graphics Pipeline Library extension to reduce shader compile stutter.\n");
            s.push_str("# WARNING: True causes known rendering bugs in Turtle WoW — leave at Auto.\n");
            s.push_str("dxvk.enableGraphicsPipelineLibrary = True\n");
            s.push('\n');
        }
        TriState::False => {
            s.push_str("# Fully disable the Graphics Pipeline Library fast-path.\n");
            s.push_str("dxvk.enableGraphicsPipelineLibrary = False\n");
            s.push('\n');
        }
    }

    match cfg.track_pipeline_lifetime {
        TriState::Auto => {} // DXVK default — omit
        TriState::True => {
            s.push_str("# Track pipeline lifetimes and free those no longer in use to reduce VRAM usage.\n");
            s.push_str("dxvk.trackPipelineLifetime = True\n");
            s.push('\n');
        }
        TriState::False => {
            s.push_str("# Disable pipeline lifetime tracking.\n");
            s.push_str("dxvk.trackPipelineLifetime = False\n");
            s.push('\n');
        }
    }

    // --- Display ---
    s.push_str("\n############################\n");
    s.push_str("# Display\n");
    s.push_str("############################\n\n");

    if cfg.enable_dialog_mode {
        s.push_str("# Run as a borderless-fullscreen window — improves alt-tab and multi-monitor behaviour.\n");
        s.push_str("d3d9.enableDialogMode = True\n");
        s.push('\n');
    }

    if !cfg.dpi_aware {
        s.push_str("# Disable DXVK's DPI awareness call — required when VanillaFixes or Wine already handle it.\n");
        s.push_str("d3d9.dpiAware = False\n");
        s.push('\n');
    }

    match cfg.present_interval {
        PresentInterval::Default => {} // -1: do not override, omit
        PresentInterval::NoSync => {
            s.push_str("# Force VSync off regardless of in-game setting (tearing may occur).\n");
            s.push_str("d3d9.presentInterval = 0\n");
            s.push('\n');
        }
        PresentInterval::Vsync => {
            s.push_str("# Force VSync on regardless of in-game setting (adds ~1 frame of latency).\n");
            s.push_str("d3d9.presentInterval = 1\n");
            s.push('\n');
        }
        PresentInterval::Half => {
            s.push_str("# Force half-rate VSync — limits to half the monitor refresh rate (e.g. 30 fps on 60 Hz).\n");
            s.push_str("d3d9.presentInterval = 2\n");
            s.push('\n');
        }
    }

    match cfg.tear_free {
        TriState::Auto => {} // DXVK default — omit
        TriState::True => {
            s.push_str("# Prevent screen tearing without the full input-latency cost of VSync.\n");
            s.push_str("dxvk.tearFree = True\n");
            s.push('\n');
        }
        TriState::False => {
            s.push_str("# Allow tearing (disables tear-free compositing entirely).\n");
            s.push_str("dxvk.tearFree = False\n");
            s.push('\n');
        }
    }

    // --- Image Quality ---
    s.push_str("\n############################\n");
    s.push_str("# Image Quality\n");
    s.push_str("############################\n\n");

    match cfg.sampler_anisotropy {
        AnisotropyLevel::NoOverride => {} // -1: let game/driver decide, omit
        AnisotropyLevel::Off => {
            s.push_str("# Disable anisotropic filtering for all samplers.\n");
            s.push_str("d3d9.samplerAnisotropy = 0\n");
            s.push('\n');
        }
        AnisotropyLevel::X2 => {
            s.push_str("# Force 2x anisotropic filtering — sharpens textures viewed at shallow angles.\n");
            s.push_str("d3d9.samplerAnisotropy = 2\n");
            s.push('\n');
        }
        AnisotropyLevel::X4 => {
            s.push_str("# Force 4x anisotropic filtering — noticeably sharper ground textures.\n");
            s.push_str("d3d9.samplerAnisotropy = 4\n");
            s.push('\n');
        }
        AnisotropyLevel::X8 => {
            s.push_str("# Force 8x anisotropic filtering — high quality, minimal performance impact.\n");
            s.push_str("d3d9.samplerAnisotropy = 8\n");
            s.push('\n');
        }
        AnisotropyLevel::X16 => {
            s.push_str("# Force 16x anisotropic filtering — maximum quality, negligible performance cost on modern GPUs.\n");
            s.push_str("d3d9.samplerAnisotropy = 16\n");
            s.push('\n');
        }
    }

    if cfg.clamp_negative_lod_bias {
        s.push_str("# Clamp negative mipmap LOD bias to zero — prevents over-sharpening that causes terrain shimmer.\n");
        s.push_str("d3d9.clampNegativeLodBias = True\n");
        s.push('\n');
    }

    // --- D3D9 Misc ---
    if cfg.defer_surface_creation || cfg.lenient_clear {
        s.push_str("\n############################\n");
        s.push_str("# D3D9 Misc\n");
        s.push_str("############################\n\n");

        if cfg.defer_surface_creation {
            s.push_str("# Defer Vulkan surface creation until the first Present call — can fix startup crashes.\n");
            s.push_str("d3d9.deferSurfaceCreation = True\n");
            s.push('\n');
        }
        if cfg.lenient_clear {
            s.push_str("# Use a fast-path clear when the rect nearly fills the render target — minor perf gain.\n");
            s.push_str("d3d9.lenientClear = True\n");
            s.push('\n');
        }
    }

    // --- Logging & HUD ---
    if !cfg.log_path.is_empty() || !cfg.hud.is_empty() {
        s.push_str("\n############################\n");
        s.push_str("# Logging & HUD\n");
        s.push_str("############################\n\n");

        if !cfg.log_path.is_empty() {
            s.push_str("# Directory for dxvk.log. \".\" = same folder as the game executable. Empty = no log file.\n");
            s.push_str(&format!("dxvk.logPath = {}\n", cfg.log_path.trim()));
            s.push('\n');
        }
        if !cfg.hud.is_empty() {
            s.push_str("# On-screen performance overlay. Comma-separate values: fps, memory, devinfo, pipelines.\n");
            s.push_str(&format!("dxvk.hud = {}\n", cfg.hud.trim()));
        }
    }

    // Tidy trailing whitespace
    let trimmed = s.trim_end().to_string();
    format!("{}\n", trimmed)
}

