impl std::fmt::Display for TriState {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            TriState::Auto => write!(f, "Auto"),
            TriState::True => write!(f, "True"),
            TriState::False => write!(f, "False"),
        }
    }
}

impl std::fmt::Display for PresentInterval {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            PresentInterval::Default => write!(f, "Don't override"),
            PresentInterval::NoSync => write!(f, "No VSync (0)"),
            PresentInterval::Vsync => write!(f, "VSync (1)"),
            PresentInterval::Half => write!(f, "Half refresh (2)"),
        }
    }
}

impl std::fmt::Display for AnisotropyLevel {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            AnisotropyLevel::NoOverride => write!(f, "No override"),
            AnisotropyLevel::Off => write!(f, "Off (0\u{00D7})"),
            AnisotropyLevel::X2 => write!(f, "2\u{00D7}"),
            AnisotropyLevel::X4 => write!(f, "4\u{00D7}"),
            AnisotropyLevel::X8 => write!(f, "8\u{00D7}"),
            AnisotropyLevel::X16 => write!(f, "16\u{00D7}"),
        }
    }
}

// ---------------------------------------------------------------------------
// Syntax highlighter for .conf preview (line-level coloring)
// ---------------------------------------------------------------------------
//
// Settings = [comment_color, key_color, muted_color, text_color]
// Highlights lines: `#` → gray comment; `key = value` → key+muted+text; other → default.

pub struct ConfHighlighter {
    comment_color: iced::Color,
    key_color: iced::Color,
    muted_color: iced::Color,
    text_color: iced::Color,
    current_line: usize,
}

impl iced::advanced::text::Highlighter for ConfHighlighter {
    type Settings = [iced::Color; 4];
    type Highlight = Option<iced::Color>;
    type Iterator<'a> = std::vec::IntoIter<(std::ops::Range<usize>, Option<iced::Color>)>;

    fn new(s: &[iced::Color; 4]) -> Self {
        Self {
            comment_color: s[0],
            key_color: s[1],
            muted_color: s[2],
            text_color: s[3],
            current_line: 0,
        }
    }

    fn update(&mut self, s: &[iced::Color; 4]) {
        self.comment_color = s[0];
        self.key_color = s[1];
        self.muted_color = s[2];
        self.text_color = s[3];
    }

    fn change_line(&mut self, line: usize) {
        self.current_line = line;
    }

    fn highlight_line(&mut self, line: &str) -> Self::Iterator<'_> {
        let len = line.len();
        if line.starts_with('#') {
            // Every comment/divider line → uniform comment color
            vec![(0..len, Some(self.comment_color))].into_iter()
        } else if let Some(eq) = line.find(" = ") {
            // key = value → three colored spans
            vec![
                (0..eq, Some(self.key_color)),
                (eq..eq + 3, Some(self.muted_color)),
                (eq + 3..len, Some(self.text_color)),
            ]
            .into_iter()
        } else {
            vec![(0..len, None)].into_iter()
        }
    }

    fn current_line(&self) -> usize {
        self.current_line
    }
}

fn conf_highlight_format(
    h: &Option<iced::Color>,
    _theme: &iced::Theme,
) -> iced::advanced::text::highlighter::Format<Font> {
    iced::advanced::text::highlighter::Format { color: *h, font: None }
}

